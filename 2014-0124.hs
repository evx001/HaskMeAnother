{-- EXTRA LIST COMPREHENSION ------------------------
note that i connects and continues in the second list generator.
[ (i,j) | i <- [1..3], j <-[i..3]] 
= 
  [(i,j)  | j <- [1..3]] ++
  [(2,j)  | j <- [2..3]] ++
  [(3,j)  | j <- [3..3]] ++
=
  [(1,1),(1,2),(1,3)] ++
  [(2,2),(2,3) ] ++
  [(3,3)] ++
= 
[(1,1),(1,2),(1,3),(2,2),(2,3),(3,3)]
--------------------------------------------
-- adding a filter
-------------------------------------------
[ (i,j) | i <- [1..3], j <-[1..3], i <= j ]
=
  [ (1,j) | j <- [1..3],1 <= j ] ++
  [ (2,j) | j <- [1..3],2 <= j ] ++
  [ (3,j) | j <- [1..3],3 <= j ]
=
  [(1,1)|1<=1]++[(1,2)|1<=2]++[(1,3)|1<=3]++
  [(2,1)|2<=1]++[(2,2)|2<=2]++[(1,3)|2<=3]++
  [(3,1)|3<=1]++[(3,2)|3<=2]++[(3,3)|3<=3]
=
  [(1,1)] ++ [(1,2)] ++ [(1,3)] ++
  []      ++ [(2,2)] ++ [(2,3)] ++
  []      ++ []      ++ [(3,3)]
=
[(1,1),(1,2),(1,3),(2,2),(2,3),(3,3)]
