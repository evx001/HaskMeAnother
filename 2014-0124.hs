  q ::= x <- l, q | b, q | * 

[e | * ] -- empty
  = [e]  
[e | x <- [l1,...,ln],q] 
  = (let x = l1 in [e | q ]) ++ ... ++ (let x = ln in [e | q ]) 

[e | b, q ] 
  = if b then [ e | q ] else []

