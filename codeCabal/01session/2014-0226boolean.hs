Boolean algebra is all about true and false. 
There are useful logical constructions that can built from this.
A Boolean "and" == && 
a Boolean "or" == || 
a Boolean "not" == /=
an Boolean Equivalence == Same Value and Same type.

	so "5" /= 5 
	but 5 == 5
 
&& means a boolean and, || means a boolean or. not negates a True or a False. 


